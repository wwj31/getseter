package main

import (
	"flag"
	"fmt"
	"go/ast"
	"log"
	"os"
	"path/filepath"
	"strings"

	"golang.org/x/tools/go/packages"
)

var (
	typeNames = flag.String("type", "", "gennerate type ")
	seter     = flag.Bool("set", false, "provide set fun of attribute")
)

func main() {
	flag.Usage = func() { fmt.Println("type error") }
	flag.Parse()
	if len(*typeNames) == 0 {
		flag.Usage()
		os.Exit(2)
	}

	types := strings.Split(*typeNames, ",")
	for _, typ := range types {
		genFile(typ)
	}
}
func genFile(typ string) {
	var (
		packName         string
		valName, valType []string
	)
	pack := parsePackage(nil, nil)
	for _, astFile := range pack.Syntax {
		for name, _ := range astFile.Scope.Objects {
			if name == typ {
				packName = astFile.Name.Name
				valName, valType = inspectAST(astFile, name)
				goto result
			}
		}
	}

result:
	outputName := filepath.Join("./", strings.ToLower(fmt.Sprintf("attr_%s.go", typ)))
	outfile(outputName, packName, typ, valName, valType)
}

// parsePackage analyzes the single package constructed from the patterns and tags.
func parsePackage(patterns []string, tags []string) *packages.Package {
	cfg := &packages.Config{
		Mode:       packages.NeedTypes | packages.NeedSyntax | packages.NeedTypesInfo,
		Tests:      false,
		BuildFlags: []string{fmt.Sprintf("-tags=%s", strings.Join(tags, " "))},
	}
	pkgs, err := packages.Load(cfg, patterns...)
	if err != nil {
		log.Fatal(err)
	}
	if len(pkgs) != 1 {
		log.Fatalf("error: %d packages found", len(pkgs))
	}
	return pkgs[0]
}

func inspectAST(file *ast.File, typeName string) (valName, valType []string) {
	var name string
	// Inspect the AST and print all identifiers and literals.
	ast.Inspect(file, func(n ast.Node) bool {
		switch x := n.(type) {
		case *ast.Ident:
			name = x.Name
		case *ast.StructType:
			if name == typeName {
				for _, field := range x.Fields.List {
					if len(field.Names) > 0 {
						valName = append(valName, field.Names[0].Name)
					} else if v, ok := field.Type.(*ast.Ident); ok {
						valName = append(valName, v.Name)
					}

					switch x := field.Type.(type) {
					case *ast.BasicLit:
						valType = append(valType, x.Value)
					case *ast.Ident:
						valType = append(valType, x.Name)
					case *ast.StarExpr:
						valType = append(valType, fmt.Sprintf("*%v", x.X.(*ast.Ident).Name))
					case *ast.MapType:
						key, _ := x.Key.(*ast.Ident)
						var val string
						switch v := x.Value.(type) {
						case *ast.Ident:
							val = v.Name
						case *ast.StarExpr:
							val = "*" + v.X.(*ast.Ident).Name
						}
						valType = append(valType, fmt.Sprintf("map[%v]%v", key, val))
					case *ast.ArrayType:
						var val string
						switch v := x.Elt.(type) {
						case *ast.Ident:
							val = v.Name
						case *ast.StarExpr:
							val = "*" + v.X.(*ast.Ident).Name
						}
						valType = append(valType, fmt.Sprintf("[]%v", val))
					}
				}
				return false
			}
		}
		return true
	})
	if len(valName) != len(valType) {
		return nil, nil
	}
	return valName, valType
}

const tplpackage = `{comment}
package {package}

`
const tplget = `
func (s *{receiver}) {funname}() {return} { return s.{val} }
`

const tplset = `func (s *{receiver}) Set{funname}(v {input}) { s.{val} = v; s.bool = true }
`

func outfile(output, packagename, struname string, valname, valtype []string) {
	_ = os.Remove(output)
	file, err := os.Create(output)
	if err != nil {
		log.Fatalf("error: %v outfile", err)
	}
	context := strings.NewReplacer([]string{
		"{comment}", fmt.Sprintf("// Code generated by \"stringer %s\"; DO NOT EDIT.\n", strings.Join(os.Args[1:], " ")),
		"{package}", packagename,
	}...).Replace(tplpackage)

	for i, val := range valname {
		r := rune(val[0])
		if r < 97 {
			continue
		}
		typ := valtype[i]

		// provide get func
		context += strings.NewReplacer([]string{
			"{receiver}", struname,
			"{funname}", strFirstToUpper(val),
			"{return}", typ,
			"{val}", val,
		}...).Replace(tplget)

		if *seter && val != "bool" {
			// provide set func
			context += strings.NewReplacer([]string{
				"{receiver}", struname,
				"{funname}", strFirstToUpper(val),
				"{input}", typ,
				"{val}", val,
			}...).Replace(tplset)
		}
	}
	_, _ = file.WriteString(context)
}
func strFirstToUpper(str string) string {
	if len(str) < 1 {
		return ""
	}
	strArry := []rune(str)
	if strArry[0] >= 97 && strArry[0] <= 122 {
		strArry[0] -= 32
	}
	return string(strArry)
}
